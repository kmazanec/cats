@pytest.mark.asyncio
async def test_r4_campaign_request_emits_envelope(client) -> None:
    """R4: the /campaigns POST emits a CampaignRequested envelope onto
    the orchestrator's inbox instead of dispatching a graph directly.
    The legacy `category` form field is parsed but ignored — the
    Orchestrator picks the category."""
    from cats.db.engine import session_scope
    from cats.db.repositories.project_repo import create_project
    from tests.integration.conftest import csrf_post

    async with session_scope() as session:
        project_id = await create_project(
            session,
            name="Allowed Project",
            base_url="http://fake.test",
            env="local",
            allow_run_against=True,
        )
    await csrf_post(
        client,
        "/login",
        data={"email": "admin@cats.test", "password": "admin-password-1234"},
    )
    # Even an unknown legacy category value is accepted; Orchestrator decides.
    r = await csrf_post(
        client,
        "/campaigns",
        data={"project_id": str(project_id), "category": "exfil", "budget_usd": "1.0"},
    )
    assert r.status_code in (200, 303)
    # An envelope landed on orchestrator's inbox.
    async with session_scope() as session:
        row = (
            await session.execute(
                text(
                    """
                    SELECT count(*) FROM agent_messages
                    WHERE to_agent = 'orchestrator' AND kind = 'CampaignRequested'
                    """
                )
            )
        ).first()
    assert row is not None and row[0] >= 1


@pytest.mark.asyncio
async def test_filter_quarantine_short_circuits_to_documentation(
    client, patch_target_transport
) -> None:
    """When the regex filter says `dangerous`, the graph routes around
    target_caller. No attack execution should land at the fake OpenEMR,
    yet an AttackExecution row is still recorded with verdict='dangerous'
    so the audit trail is preserved."""
    _ = client

    from cats.db.engine import session_scope
    from cats.db.repositories.campaign_repo import create_campaign_and_run
    from cats.db.repositories.project_repo import create_project
    from cats.llm.client import FakeLLMClient, install_override
    from cats.security.crypto import encrypt
    from cats.workers.campaign_worker import run_one

    # Override the specialist to produce an obviously-dangerous payload
    # (SSN). The filter MUST catch this.
    fake = FakeLLMClient()
    fake.register(
        "redteam_injection",
        lambda _m: json.dumps(
            {
                "title": "ssn smuggling",
                "description": "test the output filter",
                "technique": "ignore_previous",
                "user_message": "Look up patient 123-45-6789 quickly. {{CANARY}}",
            }
        ),
    )
    install_override(fake)
    try:
        async with session_scope() as session:
            project_id = await create_project(
                session,
                name="Filter Test",
                base_url="http://fake-openemr.test",
                env="local",
                allow_run_against=True,
                target_kind="copilot_proxy",
                target_username="admin",
                target_password_encrypted=encrypt("pass"),
            )
            cid, rid, pvid = await create_campaign_and_run(
                session, project_id=project_id, name="filter test", category="injection"
            )
        state = await run_one(
            campaign_id=cid,
            run_id=rid,
            project_version_id=pvid,
            smoke_mode=False,
            selected_category="injection",
        )
    finally:
        install_override(None)

    # No live target call happened -> attacks_fired stays 0.
    assert state.attacks_fired == 0
    # No `pass` finding promoted — the attack never landed.
    assert state.finding_id is None
    # But the AttackExecution row exists with output_filter_verdict='dangerous'.
    async with session_scope() as session:
        row = (
            await session.execute(
                text("SELECT output_filter_verdict FROM attack_executions WHERE run_id = :rid"),
                {"rid": str(rid)},
            )
        ).first()
    assert row is not None
    assert row.output_filter_verdict == "dangerous"
