@pytest.fixture
def patch_target_transport():
    """Patch the httpx.AsyncClient that the TargetClient instantiates so
    it routes through our fake-OpenEMR MockTransport.

    The canary is generated by the specialist, so we patch httpx at the
    moment the target_caller runs — at that point we know what canary
    to echo back. We solve this by intercepting `httpx.AsyncClient` and
    inspecting the request body to find the canary on the fly.
    """
    captured_canary: dict[str, str] = {}

    def smart_handler(request: httpx.Request) -> httpx.Response:
        path = request.url.path
        if path == "/interface/login/login.php":
            return httpx.Response(
                200,
                text=(
                    '<form action="...">\n'
                    f'<input type="hidden" name="csrf_token_form" value="fake-csrf-{uuid4()}" />\n'
                    "</form>"
                ),
                headers={"set-cookie": "PHPSESSID=fake-session; Path=/"},
            )
        if path == "/interface/main/main_screen.php":
            return httpx.Response(
                200,
                text="<html>logged in</html>",
                headers={"set-cookie": "authUser=admin; Path=/"},
            )
        if "agent.php" in path:
            # Sniff the user_message for the canary and echo it back.
            try:
                body_json = json.loads(request.content.decode("utf-8"))
                user_msg = body_json["request"]["messages"][0]["content"]
            except Exception:
                user_msg = ""
            # Pull CATS-CANARY-XXXX out of the message.
            canary = ""
            for token in user_msg.split():
                if token.startswith("CATS-CANARY-"):
                    canary = token.strip(".[]()")
                    break
            captured_canary["v"] = canary
            body = (
                f'data: {{"type":"section","content":"Sure, here you go: {canary}"}}\n\n'
                'data: {"type":"complete"}\n\n'
            )
            return httpx.Response(200, text=body)
        return httpx.Response(404, text=f"unexpected path: {path}")

    transport = httpx.MockTransport(smart_handler)

    real_client_cls = httpx.AsyncClient

    def patched_client(*args: Any, **kwargs: Any) -> httpx.AsyncClient:
        kwargs["transport"] = transport
        return real_client_cls(*args, **kwargs)

    with patch("cats.target.client.httpx.AsyncClient", patched_client):
        yield captured_canary


@pytest.mark.asyncio
async def test_full_campaign_pass_path(client, patch_target_transport) -> None:
    """Drive a campaign end-to-end. Specialist proposes -> filter passes
    -> target_caller hits fake OpenEMR -> judge rules pass on canary
    echo -> documentation writes report. Every DB row exists."""
    _ = client  # ensures DB is truncated + lifespan ran

    from cats.db.engine import session_scope
    from cats.db.repositories.campaign_repo import create_campaign_and_run
    from cats.db.repositories.project_repo import create_project
    from cats.security.crypto import encrypt
    from cats.workers.campaign_worker import run_one

    async with session_scope() as session:
        project_id = await create_project(
            session,
            name="Fake Co-Pilot",
            base_url="http://fake-openemr.test",
            env="local",
            allow_run_against=True,
            target_kind="copilot_proxy",
            target_username="admin",
            target_password_encrypted=encrypt("pass"),
        )
        cid, rid, pvid = await create_campaign_and_run(
            session,
            project_id=project_id,
            name="e2e test",
            category="injection",
        )

    state = await run_one(
        campaign_id=cid,
        run_id=rid,
        project_version_id=pvid,
        smoke_mode=False,
        selected_category="injection",
    )

    # Judge ruled pass via canary echo.
    assert state.last_verdict == "pass", f"expected pass, got {state.last_verdict!r}"
    assert state.finding_id is not None
    assert state.report_id is not None

    # All five row types written.
    async with session_scope() as session:
        rows = (
            await session.execute(
                text(
                    """
                    SELECT
                      (SELECT count(*) FROM attacks                WHERE created_in_run_id = :rid) AS atks,
                      (SELECT count(*) FROM attack_executions      WHERE run_id            = :rid) AS execs,
                      (SELECT count(*) FROM judge_verdicts)                                      AS verdicts,
                      (SELECT count(*) FROM findings               WHERE run_id            = :rid) AS findings,
                      (SELECT count(*) FROM vulnerability_reports  WHERE run_id            = :rid) AS reports
                    """
                ),
                {"rid": str(rid)},
            )
        ).first()

    assert rows is not None
    assert rows.atks >= 1
    assert rows.execs >= 1
    assert rows.verdicts >= 1
    assert rows.findings == 1
    assert rows.reports == 1


