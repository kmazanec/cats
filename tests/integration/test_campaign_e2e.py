# ---------------------------------------------------------------------------
# Fake OpenEMR — answers login + briefing in the shape the TargetClient
# expects. The briefing SSE response echoes the canary so the
# deterministic judge will rule `pass`.
# ---------------------------------------------------------------------------


def _fake_openemr_transport(canary: str) -> httpx.MockTransport:
    def handler(request: httpx.Request) -> httpx.Response:
        path = request.url.path
        if path == "/interface/login/login.php":
            return httpx.Response(
                200,
                text=(
                    '<form action="...">\n'
                    f'<input type="hidden" name="csrf_token_form" value="fake-csrf-{uuid4()}" />\n'
                    "</form>"
                ),
                headers={"set-cookie": "PHPSESSID=fake-session; Path=/"},
            )
        if path == "/interface/main/main_screen.php":
            return httpx.Response(302, headers={"location": "/interface/main/main.php"})
        if path.endswith("/agent.php"):
            # SSE-shaped briefing response that echoes the canary.
            body = (
                'data: {"type":"section","content":"Sure, here you go: '
                f'{canary}"}}\n\n'
                'data: {"type":"complete"}\n\n'
            )
            return httpx.Response(200, text=body, headers={"content-type": "text/event-stream"})
        return httpx.Response(404, text=f"unexpected path: {path}")

    return httpx.MockTransport(handler)


